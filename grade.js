var marks = 55;

if (marks >= 80) {
    console.log("Grade: A+");
} else if (marks >= 75 && marks < 80) {
    console.log("Grade: A");
} else if (marks >= 70 && marks < 75) {
    console.log("Grade: A-");
} else if (marks >= 65 && marks < 70) {
    console.log("Grade: B+");
} else if (marks >= 60 && marks < 65) {
    console.log("Grade: B");
} else if (marks >= 55 && marks < 60) {
    console.log("Grade: B-");
} else if (marks >= 50 && marks < 55) {
    console.log("Grade: C");
} else if (marks >= 40 && marks < 50) {
    console.log("Grade: D");
} else {
    console.log("Grade: F");
}
